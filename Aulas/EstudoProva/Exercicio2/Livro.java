public class Livro {

    // ATRIBUTOS
    private String nome;
    private String autor;

    // CONSTRUTOR
    public Livro(String nome, String autor) {

        this.nome = nome;
        this.autor = autor;

    }

    // GETTERS
    public String getNome() {
        return nome;
    }

    public String getAutor() {
        return autor;
    }

}
